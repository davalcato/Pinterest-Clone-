import UIKit


extension UIColor {
    
    static var mainRed: UIColor = {
        return UIColor(red: 202/255, green: 36/255, blue: 45/255, alpha: 1.0)
    }()
    
    static var darkBlue: UIColor = {
        return UIColor(red: 60/255, green: 90/255, blue: 150/255, alpha: 1.0)
    }()
    
    static var lightBlue: UIColor = {
        return UIColor(red: 70/255, green: 136/255, blue: 241/255, alpha: 1.0)
    }()
    
    static var mainGray: UIColor = {
        return UIColor(red: 80/255, green: 80/255, blue: 80/255, alpha: 1.0)
    }()
    
    static var secondaryGray: UIColor = {
        return UIColor(red: 185/255, green: 185/255, blue: 185/255, alpha: 1.0)
    }()
    
    static var offWhite: UIColor = {
        return UIColor(red: 239/255, green: 239/255, blue: 239/255, alpha: 1.0)
    }()
    
}
